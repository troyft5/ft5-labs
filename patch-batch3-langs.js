async function patch() {
  for (const lang of ['en', 'es', 'fr', 'pt', 'de', 'zh']) {
    const file = path.join(__dirname, 'messages', `${lang}.json`)
    if (fs.existsSync(file)) {
      const content = JSON.parse(fs.readFileSync(file, 'utf8'))
      content.Blog = translations[lang].Blog
      content.Industries = translations[lang].Industries
      content.Glossary = translations[lang].Glossary
      content.Partners = translations[lang].Partners
      fs.writeFileSync(file, JSON.stringify(content, null, 2))
      console.log('patched', lang)
    }
  }
}
patch()
