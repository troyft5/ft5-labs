// Map missing keys in PT/DE/ZH to EN fallback for speed, as testing goes on
const base = translations.en.Estimate
for (const lang of ['pt', 'de', 'zh']) {
  translations[lang].Estimate = { ...base, ...translations[lang].Estimate }
}

async function patch() {
  for (const [lang, data] of Object.entries(translations)) {
    const file = path.join(__dirname, 'messages', `${lang}.json`)
    if (fs.existsSync(file)) {
      const content = JSON.parse(fs.readFileSync(file, 'utf8'))
      content.Estimate = data.Estimate
      fs.writeFileSync(file, JSON.stringify(content, null, 2))
      console.log('patched', lang)
    }
  }
}
patch()
